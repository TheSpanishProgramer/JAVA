	private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnGenerarActionPerformed

		int numero1 = 0, numero2 = 0;
		// Obtener valores
		try {
			numero1 = Integer.parseInt(principio.getText());
			numero2 = Integer.parseInt(fin.getText());
		// Caso de que no se introduzca nada...
		} catch (NumberFormatException e) {
			JOptionPane.showMessageDialog(this, "Debes introducir n˙meros");
		}
		// El segundo n˙mero no puede ser mayor que el primero
		if (numero1 > numero2) {
			JOptionPane.showMessageDialog(this, "El segundo ha de ser mayor que el primero");
		} else {
			// Pasa a string el n˙mero generado
			String numeroGenerado = String.valueOf(generaNumeroAleatorio(numero1, numero2));

			// Muestra n˙mero generado
			txtNumeroGenerado.setText(numeroGenerado);
		}

	}// GEN-LAST:event_btnGenerarActionPerformed

