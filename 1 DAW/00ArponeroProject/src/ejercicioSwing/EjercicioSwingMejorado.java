	/**
	 * Genera un n˙mero aleatorio entre dos numeros. Entre el minimo y el maximo
	 * incluidos
	 */
	private int generaNumeroAleatorio(int minimo, int maximo) {

		int num = (int) Math.floor(Math.random() * (minimo - (maximo + 1)) + (maximo + 1));
		return num;
	}

