    /**
     * Devuelve el nombre del torneo y la puntuación obtenida dependiendo de la
     * categoría que tenga dicho torneo:
     * Comprueba si el valor seleccionado del comboBox es igual a alguno de los
     * valores introducidos en la clase Torneo
     * @return 
     */
    public Torneo getTorneo(){
        String nombre=this.textoNombre.getText();
        int puntuacion=0;
        String categoria = (String) this.comboCategoria.getSelectedItem();
        if(categoria.equalsIgnoreCase(Torneo.GRAND_SLAM)){
            puntuacion=2000;
        }else if(categoria.equalsIgnoreCase(Torneo.MASTER1000)){
            puntuacion=1000;
        }else if(categoria.equalsIgnoreCase(Torneo.MASTER500)){
            puntuacion=500;
        }else if(categoria.equalsIgnoreCase(Torneo.MASTER250)){
            puntuacion=250;
        }
        return new Torneo(nombre,puntuacion);
    }
