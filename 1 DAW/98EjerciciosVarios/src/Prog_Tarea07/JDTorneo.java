    /**
     * Comprueba que se halla introducido algo en 'nombre' de torneo. 
     * Si es así, oculta esta pantalla y volvemos a Principal, en caso contrario
     * nos muestra un error indicándolo
     * @param <code>evt</code> evento de clic en el botón 'Aceptar'
     */
    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        if(!this.textoNombre.getText().equals("")){
            this.haPulsadoAceptar=true;
            this.setVisible(false);
        }else{
            this.textoError.setText("El nombre del torneo no puede estar vacío");
            this.textoError.setVisible(true);
        }
    }//GEN-LAST:event_aceptarActionPerformed

