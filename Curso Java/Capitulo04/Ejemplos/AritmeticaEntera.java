class AritmeticaEntera {
  public static void main (String[] args) {
    System.out.println("Aritmética entera - Ejemplos");
    System.out.println("  Suma: (operador binario)");
    System.out.println("    125 + 356 = " + (125 + 356));
    System.out.println("  Suma: (operador unario)");
    System.out.println("    +524 = " + +524);
    System.out.println("  Resta: (operador binario)");
    System.out.println("    125 - 356 = " + (125 - 356));
    System.out.println("  Resta: (operador unario)");
    System.out.println("    -524 = " + (-524));
    System.out.println("  Multiplicación");
    System.out.println("    124 * 315 = " + (124 * 315));
    System.out.println("  División");
    System.out.println("    11 / 2 = " + (11 / 2));
    System.out.println("    11 / 3 = " + (11 / 3));
    System.out.println("    11 / 4 = " + (11 / 4));
    System.out.println("    11 / 5 = " + (11 / 5));
    System.out.println("    11 / 6 = " + (11 / 6));
    System.out.println("  Resto");
    System.out.println("    11 % 2 = " + (11 % 2));
    System.out.println("    11 % 3 = " + (11 % 3));
    System.out.println("    11 % 4 = " + (11 % 4));
    System.out.println("    11 % 5 = " + (11 % 5));
    System.out.println("    11 % 6 = " + (11 % 6));
  }
}

